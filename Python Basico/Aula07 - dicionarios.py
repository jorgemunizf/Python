# DICIONARIOS
# Os dicionários permitem que itens de uma sequência recebam índices definidos
# pelo usuário. Um dicionário contém pares de (chave, valor).
# O formato geral de um objeto dicionário é:

# {<chave 1>:<valor 1>, <chave 2>:<valor 2>, ..., <chave i>:<valor i>}

pessoas = {'111222333-44': ['João', 'Silva'], '222333444-55': ['Maria', 'Santos'], '333444555-66': ['Jorge', 'Silva']}

print(pessoas['111222333-44'])

# retorna ['João', 'Silva']

